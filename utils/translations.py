def get_text(lang_code, key, default='en'):
    try:
        return LANGUAGES.get(lang_code, LANGUAGES[default]).get(key, LANGUAGES[default].get(key, key))
    except:
        return key

def get_language_keyboard():
    from telegram import InlineKeyboardButton, InlineKeyboardMarkup
    keyboard = []
    row = []
    for code, lang in LANGUAGES.items():
        row.append(InlineKeyboardButton(f"{lang['flag']} {lang['name']}", callback_data=f"lang_{code}"))
        if len(row) == 2:
            keyboard.append(row)
            row = []
    if row:
        keyboard.append(row)
    return InlineKeyboardMarkup(keyboard)
